package Demopackage;

public class Float {
	public static void main(String[] args) {
		int intv=10;
		float floatv=10.254545554545f;
		double dbv=254545554545d;
		
		intv=22/7;
		System.out.println(intv);
		
		floatv=22f/7f;
		System.out.println(floatv);
		
		dbv=22d/7d;
		System.out.println(dbv);
		
		
		
	}

}
